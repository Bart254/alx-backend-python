#!/usr/bin/env python3
""" String concatenating module

Functions:
    concat: concatenates two strings passed as arguments
"""


def concat(str1: str, str2: str) -> str:
    """ Concatenates str1 and str2

    Args:
        str1 (str): first string
        str2 (str): second string

    Returns:
        str: the result of conacatenating str1 and str2
    """
    return str1+str2
