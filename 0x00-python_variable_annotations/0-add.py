#!/usr/bin/python3
""" Module for addition operation

Functions:
    add: returns the sum of two args passed to it
"""


def add(a: float, b: float) -> float:
    """ Adds arg(a) and arg(b)

    Args:
        a (float): First argument
        b (float): Second argument

    Returns:
        float: sum of a and b
    """
    return a+b
